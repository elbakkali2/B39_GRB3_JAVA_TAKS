public class EvenOrOdd {

    public static void main(String[] args) {


    }

    public static void evenOrOdd(int num) {
        if (num % 2 == 0) {
            System.out.println("even");
        }else if (num % 2 == 1){
            System.out.println("odd");
        }else{
            System.out.println("invalid");
        }
    }
}
